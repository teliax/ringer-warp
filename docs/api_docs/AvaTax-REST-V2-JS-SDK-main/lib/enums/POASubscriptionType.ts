import { JsonConverter, JsonCustomConvert } from "json2typescript";

/**
* @export
* @enum {string}
*/
 export enum POASubscriptionType {
        Mrs = 0,
        AvaComms = 1,
        AvaHospitality = 2,
        AvaLodging = 3,
        MRSComplianceManager = 4,
        AMRA = 5,
        MRSPremium = 6,
        MRSPremier = 7,
}

@JsonConverter
export class POASubscriptionTypeConverter implements JsonCustomConvert<POASubscriptionType> {
    serialize(data: POASubscriptionType) {
        return data;
    }
    deserialize(enumType: string): POASubscriptionType {
        return POASubscriptionType[enumType as keyof typeof POASubscriptionType];
    }
}